    public void preencherBox(boolean flag){
        
        BancoDAO objBancoDAO = new BancoDAO();
        BancoDTO objBancoDTO = new BancoDTO();
      
      if(flag){
            jcTipoConta.getSelectedIndex();
            ArrayList<BancoDTO> lista = objBancoDAO.listar();
            for(int i = 0; i < lista.size(); i++){
                objBancoDTO = lista.get(i);
                jcTipoConta.addItem(""+objBancoDTO.getIdcliente());

            }
            objBancoDTO = lista.get(jcTipoConta.getSelectedIndex());
            txtId.setText(""+objBancoDTO.getIdcliente());
            txtNumero.setText(""+objBancoDTO.getNumero());
            txtSaldo.setText(""+objBancoDTO.getSaldo());
            txtAtivo.setText(""+objBancoDTO.getAtivo());
            txtRendimento.setText(objBancoDTO.getDtrendimento());
        

      }
      else{
            objBancoDTO = lista.get(jcTipoConta.getSelectedIndex());
            txtId.setText(""+objBancoDTO.getIdcliente());
            txtNumero.setText(""+objBancoDTO.getNumero());
            txtSaldo.setText(""+objBancoDTO.getSaldo());
            txtAtivo.setText(""+objBancoDTO.getAtivo());
            txtRendimento.setText(""+objBancoDTO.getDtrendimento());
      }
    }

